package viewModel;

public class ErrorMsg {

    private String errMsgId, errMsgName, updatedBy, updatedDate;

    public String getErrMsgId() {
        return errMsgId;
    }

    public void setErrMsgId(String errMsgId) {
        this.errMsgId = errMsgId;
    }

    public String getErrMsgName() {
        return errMsgName;
    }

    public void setErrMsgName(String errMsgName) {
        this.errMsgName = errMsgName;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

    public String getUpdatedDate() {
        return updatedDate;
    }

    public void setUpdatedDate(String updatedDate) {
        this.updatedDate = updatedDate;
    }

}