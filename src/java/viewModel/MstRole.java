package viewModel;

public class MstRole {

    private String roleId, roleName, funcAreaCode, locCode;

    public String getRoleId() {
        return roleId;
    }

    public void setRoleId(String roleId) {
        this.roleId = roleId;
    }

    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getFuncAreaCode() {
        return funcAreaCode;
    }

    public void setFuncAreaCode(String funcAreaCode) {
        this.funcAreaCode = funcAreaCode;
    }

    public String getLocCode() {
        return locCode;
    }

    public void setLocCode(String locCode) {
        this.locCode = locCode;
    }
}
