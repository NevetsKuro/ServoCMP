package viewModel;

public class MessageDetails {

    private String modalTitle, modalMessage, msgClass, modalresourceMsg, fileMsg, filemsgClass, errorCode, mailMsg, mailMsgClass;
    private boolean status, mailStatus;

    public String getModalTitle() {
        return modalTitle;
    }

    public void setModalTitle(String modalTitle) {
        this.modalTitle = modalTitle;
    }

    public String getModalMessage() {
        return modalMessage;
    }

    public void setModalMessage(String modalMessage) {
        this.modalMessage = modalMessage;
    }

    public String getMsgClass() {
        return msgClass;
    }

    public void setMsgClass(String msgClass) {
        this.msgClass = msgClass;
    }

    public String getModalresourceMsg() {
        return modalresourceMsg;
    }

    public void setModalresourceMsg(String modalresourceMsg) {
        this.modalresourceMsg = modalresourceMsg;
    }

    public String getFileMsg() {
        return fileMsg;
    }

    public void setFileMsg(String fileMsg) {
        this.fileMsg = fileMsg;
    }

    public String getFilemsgClass() {
        return filemsgClass;
    }

    public void setFilemsgClass(String filemsgClass) {
        this.filemsgClass = filemsgClass;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getMailMsg() {
        return mailMsg;
    }

    public void setMailMsg(String mailMsg) {
        this.mailMsg = mailMsg;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public boolean isMailStatus() {
        return mailStatus;
    }

    public void setMailStatus(boolean mailStatus) {
        this.mailStatus = mailStatus;
    }

    public String getMailMsgClass() {
        return mailMsgClass;
    }

    public void setMailMsgClass(String mailMsgClass) {
        this.mailMsgClass = mailMsgClass;
    }

}
