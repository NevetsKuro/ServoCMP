package viewModel;

public class CustomerProductSampleDetails {

    private String tankId, productCapacity, sampleFrequency, monitorBy, presampleDate, nxtsampleDate;

    public String getTankId() {
        return tankId;
    }

    public void setTankId(String tankId) {
        this.tankId = tankId;
    }

    public String getProductCapacity() {
        return productCapacity;
    }

    public void setProductCapacity(String productCapacity) {
        this.productCapacity = productCapacity;
    }

    public String getSampleFrequency() {
        return sampleFrequency;
    }

    public void setSampleFrequency(String sampleFrequency) {
        this.sampleFrequency = sampleFrequency;
    }

    public String getMonitorBy() {
        return monitorBy;
    }

    public void setMonitorBy(String monitorBy) {
        this.monitorBy = monitorBy;
    }

    public String getPresampleDate() {
        return presampleDate;
    }

    public void setPresampleDate(String presampleDate) {
        this.presampleDate = presampleDate;
    }

    public String getNxtsampleDate() {
        return nxtsampleDate;
    }

    public void setNxtsampleDate(String nxtsampleDate) {
        this.nxtsampleDate = nxtsampleDate;
    }

}
